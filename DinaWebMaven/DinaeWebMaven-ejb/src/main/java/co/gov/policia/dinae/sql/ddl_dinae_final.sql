begin
  FOR fila IN (select SEQUENCE_NAME, MIN_VALUE, MAX_VALUE, LAST_NUMBER from USER_SEQUENCES) LOOP
    
    ssql := 'DROP sequence ' || fila.SEQUENCE_NAME;
    execute immediate ssql;
    DBMS_OUTPUT.PUT_LINE( ssql || ';');
    
